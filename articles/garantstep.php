<p><strong>В</strong><b> ноябре 2022 года вышел новый релиз Интернет-версии. Был добавлен следующий функционал:</b></p>
<p><b>ПОИСК ПО НОРМЕ ПРАВА</b></p>
<p>Новый инструмент позволяет искать документы, упоминающие нужную норму права: главу, раздел или статью кодексов и важнейших законов. В карточку поиска по реквизитам добавлен раздел «Поиск по норме права», с помощью которого можно найти документы, комментарии или судебную практику, ссылающиеся на интересующую правовую норму.</p>
<p>Если необходимы документы, ссылающиеся одновременно на несколько нормативных актов или на несколько фрагментов одного акта, добавьте еще одну поисковую строку с помощью иконки со знаком «плюс» справа и выберите логическое условие «И». Для отображения условий не забудьте поставить галочку в чекбокс «Показать логические операторы».</p>
<p>Важной частью нового функционала является возможность пересечения норм права. Используя логические операторы «И», «Или», «Кроме» в карточке запроса, можно получить список документов, одновременно упоминающих нужные статьи правовых актов, но при этом исключающие ссылки на другие фрагменты. Возможность поиска по норме доступна так же в карточке поиска по судебной практике, что открывает дополнительные возможности по работе с судебными решениями.</p>
<p>С помощью поиска по норме права можно легко находить и анализировать практически любые частные ситуации, возникающие в практике.</p>
<p><b>СОХРАНЕНИЕ ПОИСКОВЫХ ЗАПРОСОВ</b></p>
<p>Результаты запросов, найденных с помощью Базового поиска, поиска по реквизитам или полученных другими способами, теперь можно сохранить в Избранное, нажав на панели инструментов кнопку «Сохранить поисковый запрос».</p>
<p>Сохранить можно большинство запросов, наиболее часто используемых в повседневной работе:<br />
* построенные через Базовый поиск;<br />
* построенные через карточки расширенного поиска (кроме поиска по лекарственным средствам);<br />
* корреспонденты и респонденты;<br />
* построенные с применением фильтра;<br />
* построенные с помощью поиска по правовому календарю и другие.<br />
Сохраненные запросы будут помещены в Избранное на вкладку «Поисковые запросы».</p>
<p><b>ПОСТАНОВКА НА КОНТРОЛЬ ПОИСКОВЫХ ЗАПРОСОВ</b></p>
<p>Чтобы отслеживать поступление новых документов, добавленных в систему по интересующему сохраненному запросу, нужно поставить галочку в чекбокс «Поставить на контроль». При появлении нового документа в поставленных на контроль списках, система уведомит пользователя при помощи информационного сообщения на главной странице и восклицательного знака у кнопки Избранное. На вкладке «Поисковые запросы» измененные списки будут отмечены восклицательным знаком и дополнительно выделены.</p>
<p>В личном кабинете можно указать электронную почту для получения оперативной информации об изменениях. Запрос можно снять с контроля, переименовать или удалить с помощью соответствующих иконок, появляющихся справа.</p>
<p>Сохранение запросов и возможность контроля новых поступлений позволит клиентам отслеживать изменения в законодательстве, отобранные по индивидуальным потребностям.</p>
<p><b>НАСТРОЙКИ ОТКРЫТИЯ ВКЛАДОК</b></p>
<p>По многочисленным просьбам клиентов в новой версии появилась возможность настраивать открытие документов и ссылок наиболее удобным способом. В зависимости от выбранных настроек интерфейса документы и ссылки будут открываться в текущей, новой вкладке или в новом окне. Для этого в разделе «Настройки» личного кабинета нужно отметить галочками необходимые параметры.</p>
<p><b>ЭКСПОРТ И ПЕЧАТЬ НЕСКОЛЬКИХ ФРАГМЕНТОВ ДОКУМЕНТА ИЗ ОГЛАВЛЕНИЯ</b></p>
<p>В новом релизе реализована возможность сохранения в файл и печати одновременно нескольких фрагментов текста документа с помощью выделения нужных пунктов оглавления.</p>
<p>Для этого достаточно открыть оглавление интересующего документа и, удерживая кнопку CTRL, выделить те пункты, статьи, главы или разделы, которые нужно экспортировать в файл. При нажатии на кнопку «Сохранить» на панели инструментов система предложит выбрать необходимый формат. В результате в файл скопируется не весь документ, а только нужные части, отмеченные в оглавлении.</p>
<p>Для печати сразу нескольких фрагментов документа выделите их в оглавлении и нажмите кнопку «Печать»</p>
